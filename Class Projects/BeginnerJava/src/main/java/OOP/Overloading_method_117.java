
package OOP;

public class Overloading_method_117 {
    
    void sagor(int a, int b){
        System.out.println(a+b);
    }
    
    void sagor(double a, double b){
        System.out.println(a+b);
    }
    
    void sagor(int a, int b, int c){
        System.out.println(a+b+c);
    }
    
    void sagor(){
        System.out.println("Nothing to add");
    }
    
}
