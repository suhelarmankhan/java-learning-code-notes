
package com.mycompany.bebinnerjava;

public class ForLoop_37 {
    public static void main(String[] args) {
        
        for(int i = 1;i<=10; i ++){
            System.out.println(i+" Suhel Arman Khan\n");
        }
        
        
        for(int i = 10;i>=71; i --){
            System.out.println(i);
        }
        
         for(int i = 2;i<=10; i= i +2){
            System.out.println(i);
        }
         
    }
}
